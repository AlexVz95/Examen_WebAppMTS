package com.mts.actividad.vo;

public class CitasVO {
	private int idConsulta;
	private String fecha;
	private int costo;
	private String hora_consulta;
	private int idHomeopata;
	private int idMedico;
	private int idPaciente;
	public int getIdConsulta() {
		return idConsulta;
	}
	public void setIdConsulta(int idConsulta) {
		this.idConsulta = idConsulta;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	
	public int getCosto() {
		return costo;
	}
	public void setCosto(int costo) {
		this.costo = costo;
	}
	public String getHora_consulta() {
		return hora_consulta;
	}
	public void setHora_consulta(String hora_consulta) {
		this.hora_consulta = hora_consulta;
	}
	public int getIdHomeopata() {
		return idHomeopata;
	}
	public void setIdHomeopata(int idHomeopata) {
		this.idHomeopata = idHomeopata;
	}
	public int getIdMedico() {
		return idMedico;
	}
	public void setIdMedico(int idMedico) {
		this.idMedico = idMedico;
	}
	public int getIdPaciente() {
		return idPaciente;
	}
	public void setIdPaciente(int idPaciente) {
		this.idPaciente = idPaciente;
	}
	
	
}
