package com.mts.actividad.vo;

public class MedicamentosVO {
	private int idMedicamentos;
	private String nombre;
	private String codigo_med;
	
	
	public int getIdMedicamentos() {
		return idMedicamentos;
	}
	public void setIdMedicamentos(int idMedicamentos) {
		this.idMedicamentos = idMedicamentos;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getCodigo_med() {
		return codigo_med;
	}
	public void setCodigo_med(String codigo_med) {
		this.codigo_med = codigo_med;
	}
	
	
}
