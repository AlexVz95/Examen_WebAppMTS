package com.mts.actividad.vo;

public class MedicosVO {
	private int idHomeopata;
	private String nombre;
	private String cedula_ide;
	private String direccion;
	private String telefono;
	private String fecha_inicio;
	private String esMedico;
	private int idMedico;
	private String especialidad;
	public int getIdHomeopata() {
		return idHomeopata;
	}
	public void setIdHomeopata(int idHomeopata) {
		this.idHomeopata = idHomeopata;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getCedula_ide() {
		return cedula_ide;
	}
	public void setCedula_ide(String cedula_ide) {
		this.cedula_ide = cedula_ide;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	public String getFecha_inicio() {
		return fecha_inicio;
	}
	public void setFecha_inicio(String fecha_inicio) {
		this.fecha_inicio = fecha_inicio;
	}
	public String getEsMedico() {
		return esMedico;
	}
	public void setEsMedico(String esMedico) {
		this.esMedico = esMedico;
	}
	public int getIdMedico() {
		return idMedico;
	}
	public void setIdMedico(int idMedico) {
		this.idMedico = idMedico;
	}
	public String getEspecialidad() {
		return especialidad;
	}
	public void setEspecialidad(String especialidad) {
		this.especialidad = especialidad;
	}
	
	
}
